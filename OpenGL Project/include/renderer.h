void renderSprite(vec3 position, vec3 size, char* shader, char* texture)
{
    // vertex data
    Vertex data[6] =
    { 
        {.pos = {-0.5f,-0.5f,0},.uv = {0,0} },
        {.pos = { 0.5f,-0.5f,0},.uv = {1,0} },
        {.pos = {-0.5f, 0.5f,0},.uv = {0,1} },
        {.pos = { 0.5f,-0.5f,0},.uv = {1,0} },
        {.pos = {-0.5f, 0.5f,0},.uv = {0,1} },
        {.pos = { 0.5f, 0.5f,0},.uv = {1,1} },
    };

    // matrix model
    mat4 model;
    glm_mat4_identity(model);
    vec3 inversePos;
    glm_vec3_scale(position, -1, inversePos);
    glm_translate(model, position);
    glm_scale(model, size);
    //glm_translate(model, inversePos);
    setMat4(shader, "model", model);

    // set texture
    glActiveTexture(GL_TEXTURE0);
    bindTexture(texture);
    
    // create vbo
    GLuint VBO;
    glGenBuffers(1, &VBO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, 6 * sizeof(Vertex), &data[0], GL_STATIC_DRAW);

    // bind vao
    unsigned int VAO;
    glGenVertexArrays(1, &VAO);
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    
    // position attribute
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)0);
    glEnableVertexAttribArray(0);

    // texpos attribute
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);
    // draw
    glDrawArrays(GL_TRIANGLES, 0, 6);

    // delete objects
    glBindVertexArray(0);
    glDeleteBuffers(1, &VBO);
    glDeleteVertexArrays(1, &VAO);
}
