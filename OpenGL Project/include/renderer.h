void renderSprite(vec3 position, vec3 size, char* shader, char* texture)
{
    // vertex data
    Vertex data[6] =
    { 
        {.pos = {-0.5f,-0.5f,0},.uv = {0,0} },
        {.pos = { 0.5f,-0.5f,0},.uv = {1,0} },
        {.pos = {-0.5f, 0.5f,0},.uv = {0,1} },
        {.pos = { 0.5f,-0.5f,0},.uv = {1,0} },
        {.pos = {-0.5f, 0.5f,0},.uv = {0,1} },
        {.pos = { 0.5f, 0.5f,0},.uv = {1,1} },
    };

    // matrix model
    mat4 model;
    glm_mat4_identity(model);

    glm_translate(model, position);
    glm_scale(model, size);
    //glm_translate(model, inversePos);

    useShader(shader);
    setMat4(shader, "model", model);

    // set texture
    glActiveTexture(GL_TEXTURE0);
    bindTexture(texture);
    setInt(shader, "texture1", 0);
    
    // create vbo
    GLuint VBO;
    glGenBuffers(1, &VBO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, 6 * sizeof(Vertex), &data[0], GL_STATIC_DRAW);

    // bind vao
    unsigned int VAO;
    glGenVertexArrays(1, &VAO);
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    
    // position attribute
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)0);
    glEnableVertexAttribArray(0);

    // texpos attribute
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);
    // draw
    glDrawArrays(GL_TRIANGLES, 0, 6);

    // delete objects
    glBindVertexArray(0);
    glDeleteBuffers(1, &VBO);
    glDeleteVertexArrays(1, &VAO);
}
void renderLine(float* start, float* end)
{
    float vertices[] =
    {
        start[0], start[1], start[2],
        end[0], end[1], end[2],
    };

    unsigned int VBO, VAO;
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    // bind the Vertex Array Object first, then bind and set vertex buffer(s), and then configure vertex attributes(s).
    glBindVertexArray(VAO);

    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);

    // note that this is allowed, the call to glVertexAttribPointer registered VBO as the vertex attribute's bound vertex buffer object so afterwards we can safely unbind
    glBindBuffer(GL_ARRAY_BUFFER, 0);

    // You can unbind the VAO afterwards so other VAO calls won't accidentally modify this VAO, but this rarely happens. Modifying other
    // VAOs requires a call to glBindVertexArray anyways so we generally don't unbind VAOs (nor VBOs) when it's not directly necessary.
    glBindVertexArray(0);
    useShader("line");
    mat4 model;
    glm_mat4_identity(model);
    setMat4("line", "model", model);
    glBindVertexArray(VAO); // seeing as we only have a single VAO there's no need to bind it every time, but we'll do so to keep things a bit more organized
    glDrawArrays(GL_LINES, 0, 2);

    glDeleteBuffers(1, &VBO);
    glDeleteVertexArrays(1, &VAO);
}
